PSYS_FRAND_COUNT = 1024
_LOWSEED = 0x330E
_MULTIPLIER = 0x5DEECE66D
_ADDEND = 0xB
_MASK = 0x0000FFFFFFFFFFFF


class BlenderRNG:
    """Exact replica of Blender's RandomNumberGenerator (BLI_rand.hh)."""

    def __init__(self):
        self.x = 0

    def seed(self, s):
        self.x = ((s & 0xFFFFFFFF) << 16) | _LOWSEED

    def step(self):
        self.x = (_MULTIPLIER * self.x + _ADDEND) & _MASK

    def get_uint32(self):
        self.step()
        return (self.x >> 17) & 0xFFFFFFFF

    def get_int32(self):
        self.step()
        return (self.x >> 17) & 0x7FFFFFFF

    def get_float(self):
        return self.get_int32() / 0x80000000

    def seed_random(self, s):
        """BLI_rng_srandom: triple-hash seeding for better distribution."""
        s = s & 0xFFFFFFFF
        self.seed(s + _HASH_512[s & 255])
        s = self.get_uint32()
        self.seed(s + _HASH_512[s & 255])
        s = self.get_uint32()
        self.seed(s + _HASH_512[s & 255])


def generate_psys_frand_tables():
    """Generate the 3 particle random tables exactly as BKE_particle_init_rng does.

    Returns (frand_base, frand_offset, frand_multiplier) as numpy arrays.
    - frand_base: float32[1024] — base random values
    - frand_offset: uint32[1024] — per-seed offset into base table
    - frand_multiplier: uint32[1024] — per-seed multiplier for index
    """
    rng = BlenderRNG()
    rng.seed_random(5831)  # Blender's hardcoded init seed

    frand_base = np.empty(PSYS_FRAND_COUNT, dtype=np.float32)
    frand_offset = np.empty(PSYS_FRAND_COUNT, dtype=np.uint32)
    frand_multiplier = np.empty(PSYS_FRAND_COUNT, dtype=np.uint32)

    for i in range(PSYS_FRAND_COUNT):
        frand_base[i] = rng.get_float()
        frand_offset[i] = rng.get_uint32()
        frand_multiplier[i] = rng.get_uint32()

    return frand_base, frand_offset, frand_multiplier


def psys_frand(tables, psys_seed, index):
    """Replicate psys_frand() exactly: lookup with per-system seed scrambling."""
    frand_base, frand_offset, frand_multiplier = tables
    offset = frand_offset[psys_seed % PSYS_FRAND_COUNT]
    mult = frand_multiplier[psys_seed % PSYS_FRAND_COUNT]
    return frand_base[(offset + index * mult) % PSYS_FRAND_COUNT]


def generate_scrambled_table(tables, psys_seed):
    """Generate a pre-scrambled 1024-float table for a specific particle system seed.

    The GPU shader can then do simple: frandTable[index % 1024] instead of
    the full offset+multiplier lookup.
    """
    frand_base, frand_offset, frand_multiplier = tables
    offset = int(frand_offset[psys_seed % PSYS_FRAND_COUNT])
    mult = int(frand_multiplier[psys_seed % PSYS_FRAND_COUNT])
    scrambled = np.empty(PSYS_FRAND_COUNT, dtype=np.float32)
    for i in range(PSYS_FRAND_COUNT):
        scrambled[i] = frand_base[(offset + i * mult) % PSYS_FRAND_COUNT]
    return scrambled
